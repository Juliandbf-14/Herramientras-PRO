using System;

namespace Ejercicio4
{
    // 4. Imprima los números del 1 al 10 en orden ascendente
    class Program
    {
        static void Main(string[] args)
        {   
            Console.WriteLine("Numeros del 1 al 10 de forma ascendente");
            for (int i = 1; i <= 10; ++i)
            {
                Console.WriteLine(i);
            }
        }
    }
}